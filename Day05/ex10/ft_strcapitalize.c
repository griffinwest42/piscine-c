char	*ft_strcapitalize(char *str)
{
	char	*i;
	int		c;

	i = str;
	c = 1;
	while (*i)
	{
		if (*i <= 47 || *i == ' ')
			c = 1;
		else
		{
			if (c)
			{
				if (*i >= 'a' && *i <= 'z')
					*i = *i - 32;
				c = 0;
			}
			else if (*i >= 'A' && *i <= 'Z')
				*i = *i + 32;
		}
		i++;
	}
	return (str);
}
