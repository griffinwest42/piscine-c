char	*ft_strncat(char *dest, char *src, int nb)
{
	char	*i;
	int		c;

	i = dest;
	c = 0;
	while (*dest)
		dest++;
	while (*src && c < nb)
	{
		*dest = *src;
		dest++;
		src++;
		c++;
	}
	*dest = '\0';
	return (i);
}
